package linkedlist;
    
    
public class Node {
    private int value;
    private Node pointer;

    public void setValue(int value) {
        this.value = value;
    }

    public void setPointer(Node pointer) {
        this.pointer = pointer;
    }

    public int getValue() {
        return value;
    }

    public Node getPointer() {
        return pointer;
    }
    
    
    
    
    
}
